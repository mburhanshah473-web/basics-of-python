a=int(input("please enter first number: "))
b=int(input("please enter second number: "))
operation=input("please enter the operator which you want to use: ")
if(operation=="+"):
    sum=a+b
    print(f"the sum of two numbers is {sum}")
elif(operation=="-"):
    sub=a-b
    print(f"the sum of two numbers is {sub}")  
elif(operation=="*"):
    product=a*b
    print(f"the sum of two numbers is {product}")   
elif(operation=="/"):
    div=a/b
    print(f"the sum of two numbers is {div}")
                   
